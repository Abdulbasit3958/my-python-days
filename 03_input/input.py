# -----------------------------
# Input in Python
# -----------------------------

# The input() function is used when we want the user to type something.
# Example:
name = input("What is your name? : ")   # asks question and stores the answer
print(name)  # shows what the user typed


# -----------------------------
# Concatenation (joining text)
# -----------------------------
# We can add (join) text with the variable using + 
name = input("What is your name? : ")
print("Hello " + name)   # Hello gets attached with the name


# -----------------------------
# Another Example
# -----------------------------
name = input("Tony, what is your superhero name ? : ")
print("My super hero name is " + name)
