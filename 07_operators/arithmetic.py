# === Arithmetic Operators ===
print(5 + 2)    # Addition
print(5 - 2)    # Subtraction
print(5 * 2)    # Multiplication
print(5 / 2)    # Division (with decimal)
print(5 // 2)   # Division (without decimal, floor)
print(5 % 2)    # Modulus (remainder)
print(5 ** 2)   # Exponent (power)

# Shortcuts
i = 5
i = i + 2
print(i)

i = 5
i += 2  # shortcut for addition
print(i)

i -= 2  # shortcut for subtraction
print(i)

i *= 2  # shortcut for multiplication
print(i)

# Operator precedence (like BODMAS rule)
result = (2 + 3) * 5
print(result)

